public class Day11 {
    public static void main(String[] args){
        int a=7;
        int b=6;
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping : ");
        System.out.println("a = "+a);
        System.out.println("b = "+b);
    }
    
}
